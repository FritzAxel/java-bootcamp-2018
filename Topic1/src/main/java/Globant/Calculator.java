public class Calculator {
	
	/**
	 * This method return the multiplication of the parameters
	 * @param number1
	 * @param number2
	 * @return number1*number2
	 */
	public int multiplicate(int number1, int number2) {
		return number1 * number2;
	}
	
	/**
	 * This method return the addition of the parameters
	 * @param number1
	 * @param number2
	 * @return number1+number2
	 */
	public int addition(int number1, int number2) {
		return number1 + number2;
	}
	
	/**
	 * This method return the subtraction of the parameters
	 * @param number1
	 * @param number2
	 * @return number1-number2
	 */
	public int subtraction(int number1, int number2) {
		return number1 - number2;
	}
	

}